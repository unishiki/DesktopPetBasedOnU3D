public class idleResetState : StateMachineBehaviour
{
    // OnStateEnter is called when a transition starts and the state machine starts to evaluate this state
    override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        
        // 随机动作全部取消
        animator.SetBool("rdm01", false);
        animator.SetBool("rdm02", false);
        animator.SetBool("rdm03", false);
        animator.SetBool("rdm04", false);
        animator.SetBool("bow", false);
        animator.SetBool("exit", false);

        if (UIScript.Instance.randomAnimMode)
        {
            animator.SetBool("needResetPos", true);
        }
    }

